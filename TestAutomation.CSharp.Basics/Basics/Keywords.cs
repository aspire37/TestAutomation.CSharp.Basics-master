using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TestAutomation.CSharp.Basics.Basics
{
    public class Keywords
    {
        /// Modifiers: Modifier keywords are specific keywords that indicate who can modify types and type members.
        // 1. abstract
        // 2. async
        // 3. const
        // 4. event
        // 5. extern
        // 6. new
        // 7. override
        // 8. partial
        // 9. readonly
        // 10. sealed
        // 11. static
        // 12. unsafe
        // 13. virtual
        // 14. volatile

        /// Access Modifier Keywords: Access modifiers are applied to the declaration of the class, method, properties, fields, and other members.
        // public - The Public modifier allows any part of the program in the same assembly or another assembly to access the type and its members.
        // private - The Private modifier restricts other parts of the program from accessing the type and its members. Only code in the same class or struct can access it.
        // internal- The Internal modifier allows other program code in the same assembly to access the type or its members. This is default access modifiers if no modifier is specified.
        // protected - The Protected modifier allows codes in the same class or a class that derives from that class to access the type or its members.

        /// Statements: Statement keywords are related to program flow.
        // 1. if
        // 2. else
        // 3. switch
        // 4. case
        // 5. do
        // 6. for
        // 7. foreach
        // 8. in
        // 9. while
        // 10. break
        // 11. continue
        // 12. default
        // 13. goto
        // 14. return
        // 15. yield
        // 16. throw
        // 17. try
        // 18. catch
        // 19. finally
        // 20. checked
        // 21. unchecked
        // 22. fixed
        // 23. lock

        // Method Parameter Keywords: These keywords are applied to the parameters of a method.
        // 1. params
        // 2. ref
        // 3. out

        // Namespace Keywords:These keywords are applied with namespace and related operators.
        // 1. using
        // 2. . operator
        // 3. :: operator
        // 4. extern alias

        // Operator Keywords: Operator keywords perform miscellaneous actions.
        // 1. as
        // 2. await
        // 3. is
        // 4. new
        // 5. sizeof
        // 6.typeof
        // 7. stackalloc
        // 8. checked
        // 9. unchecked

        // Access Keywords: Access keywords are used to access the containing class or the base class of an object or class.
        // 1. base
        // 2. this

        // Literal Keywords: Literal keywords apply to the current instance or value of an object.
        // 1. null
        // 2. false
        // 3.true
        // 4. value
        // 5. void

        // Type Keywords: Type keywords are used for data types.
        // 1. bool
        // 2. byte
        // 3. char
        // 4. class
        // 5. decimal
        // 6. double
        // 7. enum
        // 8. float
        // 9. int
        // 10. long
        // 11. sbyte
        // 12. short
        // 13. string
        // 14. struct
        // 15. uint
        // 16. ulong
        // 17. ushort

        // Contextual Keywords: Contextual keywords are considered as keywords, only if used in specific contexts.
        // 1. add
        // 2. var
        // 3. dynamic
        // 4. global
        // 5. set
        // 6.value

        // Query Keywords:
        // 1. from
        // 2. where
        // 3. select
        // 4. group
        // 5. into
        // 6. orderby
        // 7. join
        // 8. let
        // 9. in
        // 10. on
        // 11. equals
        // 12. by
        // 13. ascending
        // 14. descending
    }

}
